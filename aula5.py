# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existe duas respostas possíveis:
# sim (True) ou não (False).
# Existem vários operadores para "questionar".capitalizeDentre eles o ==, que é um operador lógico que quistiona se um valor é igual a outro.
print(10 == 10) # Sim => True (Verdadeiro)
print(5 < 2) #Não => False (Falso)
print(type(10 == 10))
print(type(5 < 2))